package com.study.demo;
/**
 * 表述一个人的基本信息【基础类型】
 * @author luozhipeng
 * @date   2020年6月21日
 */
public class Demo2 {

	public static void main(String[] args) {
		// 人的信息
		//sysout快捷方式
		System.out.println("姓名"); //输出姓名：字符串
		System.out.println("18");     //输出年龄:int类型   //数字加了引号则为字符串
		System.out.println(3000.58);   //月薪：double类型，小数点
		System.out.println("地址"); //住址：字符串：String
		//Shift+Ctrl+F:格式化
		System.out.println("爱好");//爱好：字符串String
		System.out.println("性别");  //char类型
		System.out.println("https://www.baidu.com");

	}

}
